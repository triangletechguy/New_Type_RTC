export function normalizeSettingsLanguage(language) {
  return settingsLanguageOptions.includes(language) ? language : 'English'
}

export function translateApp(language, key, replacements = {}) {
  const normalizedLanguage = normalizeSettingsLanguage(language)
  const template = settingsTranslations[normalizedLanguage]?.[key]
    || appTranslations[normalizedLanguage]?.[key]
    || expandedAppTranslations[normalizedLanguage]?.[key]
    || settingsCopy[key]
    || appCopy[key]
    || key

  return Object.entries(replacements).reduce(
    (text, [name, value]) => text.replaceAll(`{${name}}`, value),
    template
  )
}

export function languageStatus(language) {
  const normalizedLanguage = normalizeSettingsLanguage(language)
  const translatedLanguage = translateApp(normalizedLanguage, normalizedLanguage)
  return `${translatedLanguage} ${translateApp(normalizedLanguage, 'selected.')}`
}

export function readStoredSettingsLanguage() {
  if (typeof window === 'undefined') return 'English'
  try {
    const saved = JSON.parse(window.localStorage.getItem('rtc_room_settings') || '{}')
    return normalizeSettingsLanguage(saved?.language || 'English')
  } catch {
    return 'English'
  }
}

export function writeStoredSettingsLanguage(language) {
  if (typeof window === 'undefined') return
  const normalizedLanguage = normalizeSettingsLanguage(language)
  try {
    const saved = JSON.parse(window.localStorage.getItem('rtc_room_settings') || '{}')
    const next = saved && typeof saved === 'object' ? { ...saved, language: normalizedLanguage } : { language: normalizedLanguage }
    window.localStorage.setItem('rtc_room_settings', JSON.stringify(next))
  } catch {
    window.localStorage.setItem('rtc_room_settings', JSON.stringify({ language: normalizedLanguage }))
  }
}

